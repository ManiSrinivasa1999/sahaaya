// Function to get translation with fallback
window.getTranslation = function(key, language = 'en') {
    return window.translations[key]?.[language] || window.translations[key]?.['en'] || key;
};

// Function to translate an element
window.translateElement = function(element, key, language = 'en') {
    const translation = window.getTranslation(key, language);
    
    if (element.tagName.toLowerCase() === 'input' || element.tagName.toLowerCase() === 'textarea') {
        element.placeholder = translation;
    } else {
        element.textContent = translation;
    }
};

// Function to translate all elements on page
window.translatePage = function(language = 'en') {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        window.translateElement(element, key, language);
    });
    
    const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        element.placeholder = window.getTranslation(key, language);
    });
};